package course;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
	
		double n1 = 10;
		double n2 = 20;
		String n3 = "Daniel";
		
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println("--------------------------------------");
		
		n1 *= 2;
		n2 += n2;
		n3 += " Novais";
		
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);

	}

}
