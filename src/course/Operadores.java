package course;

public class Operadores {

	public static void main(String[] args) {
		// Estudando operadores em java
		
		int n1 = 3 + 4 * 2;
		int n2 = (3 + 4) * 2;
		int n3 = 17 % 3; // % é o mod: resto da divisão
		double n4 = (double) 10 / 8; // fazer o casting, senão perde dados. ou colocar os numeros com casa decimal.
		
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println(n4);

	}

}
