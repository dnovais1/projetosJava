package course;

public class Aula21_OperadoresComparativos {

	public static void main(String[] args) {

		int a = 10;
		boolean c1 = a < 10;
		boolean c2 = a > 20;
		boolean c3 = a == 10;
		boolean c4 = a != 10;
		
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);

	}

}
